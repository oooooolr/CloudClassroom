package com.sdjt.classroom.model;

public class Certificate_format {
    private Integer id;
    private String cer_name;
    private String cer_url;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCer_name() {
        return cer_name;
    }

    public void setCer_name(String cer_name) {
        this.cer_name = cer_name;
    }

    public String getCer_url() {
        return cer_url;
    }

    public void setCer_url(String cer_url) {
        this.cer_url = cer_url;
    }
}
